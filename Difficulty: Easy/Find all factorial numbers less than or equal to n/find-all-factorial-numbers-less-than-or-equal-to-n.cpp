class Solution {
  public:
    vector<long long> factorialNumbers(long long n) {
        // Write Your Code here
        vector<long long> answer;
        long long result=1,x=1;
        while(result<=n){
            x=x+1;
            answer.push_back(result);
            result=x*result;
        }
        return answer;
    }
};

