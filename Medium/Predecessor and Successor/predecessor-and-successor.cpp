// This function finds predecessor and successor of key in BST.
// It sets pre and suc as predecessor and successor respectively
class Solution
{
    public:
    void findPreSuc(Node* root, Node*& pre, Node*& suc, int key)
    {
        // Initialize predecessors and successors as NULL.
        pre = nullptr;
        suc = nullptr;

        Node* temp = root;

        // Traverse the tree to find the node with the given key.
        while (temp != nullptr) {
            // If current node's key is equal to the given key.
            if (temp->key == key) {
                // If the node has a left subtree, its predecessor will be the rightmost node in the left subtree.
                if (temp->left != nullptr) {
                    pre = temp->left;
                    while (pre->right != nullptr)
                        pre = pre->right;
                }
                // If the node has a right subtree, its successor will be the leftmost node in the right subtree.
                if (temp->right != nullptr) {
                    suc = temp->right;
                    while (suc->left != nullptr)
                        suc = suc->left;
                }
                // Break the loop once the node with the given key is found.
                break;
            }
            // If the current node's key is greater than the given key, update the successor and move to the left subtree.
            else if (temp->key > key) {
                suc = temp;
                temp = temp->left;
            }
            // If the current node's key is less than the given key, update the predecessor and move to the right subtree.
            else {
                pre = temp;
                temp = temp->right;
            }
        }
    }
};

